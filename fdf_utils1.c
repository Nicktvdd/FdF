#include "fdf.h"

void free_map(char ***map)
{
    int i = 0, j = 0;

    while (map[i] != NULL)
    {
        j = 0;
        while (map[i][j] != NULL)
        {
            free(map[i][j]);
            j++;
        }
        free(map[i]);
        i++;
    }
    free(map);
}